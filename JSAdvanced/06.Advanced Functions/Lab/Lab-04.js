function solve(data, criteria) {
    let array = JSON.parse(data);

    if (criteria === 'all') {
        array.forEach((e , index) => console.log(`${index}. ${e.first_name} ${e.last_name} - ${e.email}`))
    } else {
        let [field , param] = criteria.split('-');
        array.filter(e => e[field] === param)
            .forEach((e, index) => console.log(`${index}. ${e.first_name} ${e.last_name} - ${e.email}`))
    }
}

