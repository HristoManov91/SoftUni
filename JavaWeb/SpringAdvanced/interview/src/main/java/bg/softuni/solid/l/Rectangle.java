package bg.softuni.solid.l;

public class Rectangle {

  private int width, height;

  public int getWidth() {
    return width;
  }

  public Rectangle setWidth(int width) {
    this.width = width;
    return this;
  }

  public int getHeight() {
    return height;
  }

  public Rectangle setHeight(int height) {
    this.height = height;
    return this;
  }
}
